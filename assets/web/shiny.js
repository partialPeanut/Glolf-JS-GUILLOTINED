// Playback Buttons:
// playback1 - Pauses Game Playback
// playback2 - Resumes Game Playback
// playback3 - Steps Back To Previous Event
// playback4 - Steps Ahead To Next Event
// playback5 - Changes Playback Speed

// Playback Constants & Variables
const playback1 = document.getElementById("playback1");
const playback2 = document.getElementById("playback2");
const playback3 = document.getElementById("playback3");
const playback4 = document.getElementById("playback4");
const playback5 = document.getElementById("playback5");
const playbackButtons = [playback1, playback2, playback3, playback4, playback5];

let playbackSpeed = 1
let playActive = false
let playbackTimer
let speed = 1000

reports = []
pastReports = -1


function playbackPressed(id) {
    let buttonPressed = playbackButtons[id]
    if (buttonPressed == playback1) pauseTime()
    if (buttonPressed == playback2) resumeTime()
    if (buttonPressed == playback3) prevEvent()
    if (buttonPressed == playback4) nextEvent()
    if (buttonPressed == playback5) changePlaybackSpeed()
}

// playback1
function pauseTime() {
    clearInterval(playbackTimer)
    playbackTimer = null
}

// playback2
function resumeTime() {
    updateInterval()
}

// playback3
function prevEvent() {
    // Figure it out later
}

// playback4
function nextEvent() {
    Greedler.doTimeStep()
    let player1 = activePlayerOnTimeline(Onceler.currentWorldState, 0)
    let player2 = activePlayerOnTimeline(Onceler.currentWorldState, 1)
    let player3 = activePlayerOnTimeline(Onceler.currentWorldState, 2)
    let player4 = activePlayerOnTimeline(Onceler.currentWorldState, 3)
    if (reports.length > 0) pastReports = reports.length
    reports = Onceler.mostRecentReports()
    // Handles Transforming the Feed
    document.getElementById("temp-pd-1").textContent = playerStatsToString(player1)[0]
    document.getElementById("temp-px-1").textContent = playerStatsToString(player1)[1]
    document.getElementById("temp-pd-2").textContent = playerStatsToString(player2)[0]
    document.getElementById("temp-px-2").textContent = playerStatsToString(player2)[1]
    document.getElementById("temp-pd-3").textContent = playerStatsToString(player3)[0]
    document.getElementById("temp-px-3").textContent = playerStatsToString(player3)[1]
    document.getElementById("temp-pd-4").textContent = playerStatsToString(player4)[0]
    document.getElementById("temp-px-4").textContent = playerStatsToString(player4)[1]
    if (reports.length != pastReports) {
        if (reports.length == 1) switchFeedsToOne()
        else if (reports.length > 1) switchFeedsToFour()
    }
    if (reports.length == 1) {
        document.getElementById("feed-displayer0").textContent = reports[0]
    }
    else {       
        document.getElementById("feed-displayer1").textContent = reports[0]
        document.getElementById("feed-displayer2").textContent = reports[1]
        document.getElementById("feed-displayer3").textContent = reports[2]
        document.getElementById("feed-displayer4").textContent = reports[3]
    }
}

// playback5
function changePlaybackSpeed() {
    if (playbackSpeed < 3) {
        playbackSpeed++;
        if (playbackSpeed == 2) {
            playback5.lastChild.textContent = "F";
            playback5.style.paddingLeft = "4px";
            speed = 500
            if (playbackTimer) {
                updateInterval()
            }
        }
        else {
            if (playbackSpeed == 3) playback5.lastChild.textContent = " G";
            playback5.style.paddingLeft = "8px";
            speed = 1
            if (playbackTimer) {
                updateInterval()
            }
        }
    }
    else {
        playbackSpeed = 1;
        playback5.lastChild.textContent = "E";
        playback5.style.paddingLeft = "0px";
        speed = 1000
        if (playbackTimer) {
            updateInterval()
        }
    }
}

// additional playback functions
function updateInterval() {
    if (!playbackTimer) {
        playbackTimer = setInterval(nextEvent, speed)
    }
    else {
        clearInterval(playbackTimer)
        playbackTimer = setInterval(nextEvent, speed)
    }
}

